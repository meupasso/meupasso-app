import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const blogDir = path.join(__dirname, "..", "content", "blog");

const files = fs.readdirSync(blogDir).filter((f) => f.endsWith(".mdx"));
const entries = files.map((file) => {
  const raw = fs.readFileSync(path.join(blogDir, file), "utf-8");
  const match = raw.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  const data = {};
  let content = "";
  if (match) {
    match[1].split("\n").forEach((line) => {
      const idx = line.indexOf(":");
      if (idx === -1) return;
      const key = line.slice(0, idx).trim();
      let val = line.slice(idx + 1).trim();
      val = val.replace(/^"(.*)"$/, "$1").replace(/^'(.*)'$/, "$1");
      if (key === "tags") {
        try { data[key] = JSON.parse(val); } catch { data[key] = []; }
      } else {
        const num = Number(val);
        data[key] = !isNaN(num) && val !== "" ? num : val;
      }
    });
    content = match[2] || "";
  }
  return {
    imagem: data.imagem || "",
    titulo: data.titulo || file,
    slug: data.slug || file.replace(".mdx", ""),
    descricao: data.descricao || "",
    data: data.data || "",
    tags: Array.isArray(data.tags) ? data.tags : [],
    content,
  };
}).sort((a, b) => b.data.localeCompare(a.data));

const ts = `// GENERATED BY scripts/generate-blog-registry.mjs

export type BlogPost = {
  titulo: string;
  slug: string;
  imagem: string;
  descricao: string;
  data: string;
  tags: string[];
  content: string;
};

export const blogRegistry: BlogPost[] = ${JSON.stringify(entries, null, 2)};
`;

fs.writeFileSync(path.join(__dirname, "..", "lib", "blog-data.ts"), ts, "utf-8");
console.log(`✅ lib/blog-data.ts gerado com ${entries.length} posts`);
