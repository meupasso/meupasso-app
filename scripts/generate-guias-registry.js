/**
 * Build-time script: lê os arquivos MDX de content/guias/
 * e gera lib/guias-data.ts com todo o conteúdo embutido,
 * para funcionar em serverless (Vercel) sem fs runtime.
 */
const fs = require("fs");
const path = require("path");

const contentDir = path.join(__dirname, "..", "content", "guias");
const outputFile = path.join(__dirname, "..", "lib", "guias-data.ts");

function extractFrontmatter(raw) {
  const match = raw.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!match) return { data: {}, content: raw };
  const frontmatter = {};
  match[1].split("\n").forEach((line) => {
    const idx = line.indexOf(":");
    if (idx === -1) return;
    const key = line.slice(0, idx).trim();
    let val = line.slice(idx + 1).trim();
    // Remove quotes
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    const num = Number(val);
    frontmatter[key] = isNaN(num) ? val : num;
  });
  return { data: frontmatter, content: match[2] };
}

function build() {
  const linguagens = fs.readdirSync(contentDir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  const registry = {};

  for (const lang of linguagens) {
    const langDir = path.join(contentDir, lang);
    const files = fs.readdirSync(langDir).filter((f) => f.endsWith(".mdx"));
    const topicos = [];

    for (const file of files) {
      const raw = fs.readFileSync(path.join(langDir, file), "utf-8");
      const { data, content } = extractFrontmatter(raw);
      topicos.push({
        titulo: data.titulo || file,
        slug: data.slug || file.replace(".mdx", ""),
        ordem: data.ordem ?? 999,
        content: content || "",
      });
    }

    topicos.sort((a, b) => a.ordem - b.ordem);
    registry[lang] = topicos;
  }

  const ts = `// GENERATED BY scripts/generate-guias-registry.js
// DO NOT EDIT — run \`node scripts/generate-guias-registry.js\` to regenerate.

export type TopicoData = {
  titulo: string;
  slug: string;
  ordem: number;
  content: string;
};

export const guiasRegistry: Record<string, TopicoData[]> = ${JSON.stringify(registry, null, 2)};
`;

  fs.writeFileSync(outputFile, ts, "utf-8");
  const count = Object.values(registry).flat().length;
  console.log(`✅ Generated ${outputFile} with ${count} tópicos across ${linguagens.length} linguagens.`);
}

build();
